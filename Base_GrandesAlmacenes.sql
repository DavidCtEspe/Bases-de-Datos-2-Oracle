
CREATE TABLE CAJEROS (
CODIGO INT,
NOMAPELS VARCHAR(30),
PRIMARY KEY (CODIGO)
);
CREATE TABLE PRODUCTOS(
CODIGO INT,
NOMBRE VARCHAR(100),
PRECIO FLOAT,
PRIMARY KEY (CODIGO)
);
CREATE TABLE MAQUINAS_REGISTRADORA(
CODIGO INT,
PISO INT,
PRIMARY KEY (CODIGO)
);
CREATE TABLE VENTA(
CAJERO INT,
MAQUINA INT,
PRODUCTO INT,
CONSTRAINT FK_C FOREIGN KEY (CAJERO)REFERENCES CAJEROS (CODIGO),
CONSTRAINT FK_M FOREIGN KEY (MAQUINA)REFERENCES PRODUCTOS (CODIGO),
CONSTRAINT FK_P FOREIGN KEY (PRODUCTO)REFERENCES MAQUINAS_REGISTRADORA (CODIGO)
);

